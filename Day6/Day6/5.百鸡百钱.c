#include<stdio.h>
#include<stdlib.h>
/*
	现有100元钱,要买100只鸡
		公鸡1只5元,母鸡一只3元,小鸡1元3只,问有多少种买法?
	穷举法:
		1.先假定公鸡的数量(0 - 能取的最大值),然后再穷举母鸡的数量(0 - 能取的最大值),通过前面再次穷举,结合条件得出小鸡的数量,然后判断这种买法在数量上和金钱上是否满足条件.
*/
void main() {
	for (int i = 0; i <= 100 / 5; i++)
	{
		for (int j = 0; j <= 100 / 3; j++)
		{
			if ((100 - i - j) % 3 == 0 && (5 * i + 3 * j + (100 - i - j) / 3 == 100)) {
				printf("%d只公鸡,%d只母鸡,%d只小鸡\n",i,j,(100 - i - j));
			}
		}
	}
	system("pause");
}